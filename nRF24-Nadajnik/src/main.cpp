#include <SPI.h>
#include <nRF24L01.h>
#include <RF24.h>

RF24 radio(9, 10); // CE, CSN
const byte address[6] = "00001";
void setup() {
  radio.begin();
  radio.openWritingPipe(address);
  radio.setPALevel(RF24_PA_LOW);
  radio.stopListening();
}
int data = 100;
void loop() {
  const char text[] = "0101010101";
  data++;
  radio.write(&data, sizeof(data));
  delay(4);
}